export function getInvestTheme(slug: string): InvestTheme | undefined {
  return investThemes.find((t) => t.slug === slug);
}

export function getRelatedInvestThemes(currentSlug: InvestThemeSlug): InvestTheme[] {
  const current = getInvestTheme(currentSlug);
  if (!current) return [];
  return current.relatedTopics
    .map((s) => getInvestTheme(s))
    .filter((t): t is InvestTheme => Boolean(t));
}
