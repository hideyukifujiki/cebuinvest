export function getLiveBasic(slug: string): LiveBasic | undefined {
  return liveBasics.find((b) => b.slug === slug);
}

export function getRelatedLiveBasics(currentSlug: LiveBasicSlug): LiveBasic[] {
  const current = getLiveBasic(currentSlug);
  if (!current) return [];
  return current.relatedTopics
    .map((s) => getLiveBasic(s))
    .filter((b): b is LiveBasic => Boolean(b));
}
