外国人がフィリピンで事業をする場合、法人形態、外資規制、就労ビザ、税務、営業許可を軽く見てはいけません。
小さく始める場合でも、売上が立ち始めると、契約書、請求書、税務申告、スタッフ雇用、ビザの問題が出てきます。
特に日本人本人が現地で業務を行う場合、適切なビザや就労資格が必要になる可能性があります。
最初は小さな事業でも、後から整えるのではなく、最初から最低限の法務・税務設計をしておく方が安全です。
