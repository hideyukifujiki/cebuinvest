オフィス型ビジネスでは、通行人がそのまま顧客になるケースは少ないです。ウェブサイト、紹介、SNS、広告、既存ネットワーク、法人営業など、顧客獲得の導線を別に設計する必要があります。
特に日本人向けビジネスの場合、セブ現地だけでなく、日本からの問い合わせ導線をどう作るかが重要です。
